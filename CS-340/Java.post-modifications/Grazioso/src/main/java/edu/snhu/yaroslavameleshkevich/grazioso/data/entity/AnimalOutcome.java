package edu.snhu.yaroslavameleshkevich.grazioso.data.entity;

public enum AnimalOutcome {

    /**
     * Represents an outcome where an animal has been adopted.
     */
    ADOPTION,

    /**
     * Represents an outcome where an animal has been euthanized.
     */
    EUTHANASIA,

    /**
     * Represents an outcome where an animal is returned to its original owner.
     */
    RETURN_TO_OWNER,

    /**
     * Represents an outcome where an animal has been transferred to a different facility
     * or organization.
     */
    TRANSFER,
}
